    private void botonActualizarPropuestaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActualizarPropuestaActionPerformed
        
        String id = this.campoIdPropuesta.getText();
        ClsPropuesta propuesta = this.BuscarPropuesta(id);
        
        String sector = this.comboSector.getSelectedItem().toString();
        propuesta.setSector(sector);
        
        String descripcion = this.campoDescripcionPropuesta.getText();
        propuesta.setDescripcion(descripcion);
        
        ClsMensaje mensaje = this.controlador.ActualizarPropuesta(propuesta);
        
        if (mensaje.getTipo().equals(mensaje.OK)) {
            int col = 0;
            int ro = this.tablaCandidatos.getSelectedRow();
            String cedula = this.tablaCandidatos.getModel().getValueAt(ro, col).toString();
            ClsCandidato candidato = this.BuscarCandidato(cedula);
            propuestas = this.controlador.ObtenerPropuestas(cedula);
            if (propuestas != null) {
                candidato.setPropuestas(propuestas);
                this.ActualizarTablaPropuestas(candidato.getPropuestas());
                this.campoDescripcionPropuesta.setText("");
                this.botonActualizarPropuesta.setVisible(false);
                this.botonNuevoPropuesta.setVisible(false);
                this.botonAgregarPropuesta.setVisible(true);
            }
        }
        JOptionPane.showMessageDialog(rootPane, mensaje.getDescripcion());
    }//GEN-LAST:event_botonActualizarPropuestaActionPerformed

